package com.Ashow.type;

public class TempTable {
	private int ID;
	private int TempID;
	private double Temp;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getTempID() {
		return TempID;
	}
	public void setTempID(int tempID) {
		TempID = tempID;
	}
	public double getTemp() {
		return Temp;
	}
	public void setTemp(double temp) {
		Temp = temp;
	}

}
